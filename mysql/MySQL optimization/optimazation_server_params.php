    Нам часто задают вопросы примерно такого плана: «Как оптимально задать конфигурационные параметры для моего сервера с 16 Гбайт памяти и объемом данных 100 Гбайт?» Но на этот вопрос нет однозначного ответа. Конфигурация сервера сильно зависит от оборудования, объема данных, типичных запросов и требований к системе.
